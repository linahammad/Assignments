    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        count++;
        if (count==1)
        {
        tm = new Team();
        updateLabels();
        updateTextArea();
        }
        else
            JOptionPane.showMessageDialog(null, "The team has been already created");
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       if (count==0)
           JOptionPane.showMessageDialog(null, "You have to create a team before");
       else
       {
           if(tm.getCount() == tm.getSize())
               JOptionPane.showMessageDialog(null, "Sorry.. the team is full, you can NOT add a new player","",JOptionPane.ERROR_MESSAGE);
           else
           {Player p1 = tm.createPlayer();
            tm.addPlayer(p1);}
            updateLabels();
            updateTextArea();
       }
    }//GEN-LAST:event_jButton2ActionPerformed

